//User function Template for C++
class Solution
{
public:
    vector<long long> factorialNumbers(long long N)
    {
        // Write Your Code here.
        long long int fact, temp;
        fact = 1, temp = 2;
        vector<long long> ans;
        while(fact <= N) {
            ans.push_back(fact);
            fact = fact * temp;
            temp++;
        }
        return ans;
    }
};

